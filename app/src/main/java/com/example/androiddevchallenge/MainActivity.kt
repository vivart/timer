@Composable
fun MyApp() {
    Scaffold(
        topBar = {
            TopAppBar(
                title = { Text(stringResource(R.string.title)) },
            )
        },
        content = { innerPadding ->
            TimerScreen(modifier = Modifier.padding(innerPadding))
        }
    )
}

